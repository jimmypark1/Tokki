package com.Whowant.Tokki.VO;

public class BookListVo {
    int BOOKS_COUNT;
    int ID;
    String READING_NAME;

    public int getBOOKS_COUNT() {
        return BOOKS_COUNT;
    }

    public void setBOOKS_COUNT(int BOOKS_COUNT) {
        this.BOOKS_COUNT = BOOKS_COUNT;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getREADING_NAME() {
        return READING_NAME;
    }

    public void setREADING_NAME(String READING_NAME) {
        this.READING_NAME = READING_NAME;
    }
}
